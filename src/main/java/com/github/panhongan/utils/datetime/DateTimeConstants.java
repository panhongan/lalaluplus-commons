public class DateTimeConstants {

    public static final int HALF_HOUR_SEC = 1800;

    public static final int ONE_HOUR_SEC = 3600;

    public static final int SIX_HOURS_SEC = ONE_HOUR_SEC * 6;

    public static final int TWELVE_HOURS_SEC = ONE_HOUR_SEC * 12;

    public static final int ONE_DAY_SEC = 3600 * 24;

    public static final int ONE_AND_A_HALF_DAYS_SEC = (int) (ONE_DAY_SEC * 1.5);

    public static final int TWO_DAYS_SEC = ONE_DAY_SEC * 2;

    public static final int THREE_DAYS_SEC = ONE_DAY_SEC * 3;

    public static final int FOUR_DAYS_SEC = ONE_DAY_SEC * 4;

    public static final int SEVEN_DAYS_SEC = ONE_DAY_SEC * 7;

    public static final int TEN_DAYS_SEC = ONE_DAY_SEC * 10;

    public static final int FIFTEEN_DAYS_SEC = ONE_DAY_SEC * 15;

    public static final int THIRTY_DAYS_SEC = ONE_DAY_SEC * 30;

    public static final int NINETY_DAYS_SEC = ONE_DAY_SEC * 90;
}
